CREATE OR REPLACE FUNCTION public.mis_roles()
RETURNS TEXT[] LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public AS $$
  SELECT COALESCE(array_agg(r.rol_nombre), ARRAY[]::TEXT[])
  FROM usuario u
  JOIN usuario_rol ur ON ur.usr_id = u.usr_id
  JOIN rol r ON r.rol_id = ur.rol_id
  WHERE u.auth_user_id = auth.uid() AND u.usr_estado = 'activo';
$$;

