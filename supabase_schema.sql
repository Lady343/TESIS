-- TABLA: notificacion_alerta
-- Registra cada notificación enviada o programada a 24h
-- ============================================================
CREATE TABLE IF NOT EXISTS notificacion_alerta (
  nal_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tic_id BIGINT NOT NULL REFERENCES ticket(tic_id) ON DELETE CASCADE,
  nal_tipo TEXT NOT NULL,            -- 'despachado_con_alerta' | 'pendiente_expiracion'
  nal_nivel_riesgo TEXT NOT NULL,    -- 'Medio' | 'Alto'
  nal_estado TEXT NOT NULL DEFAULT 'pendiente',  -- 'pendiente' | 'enviada' | 'fallida'
  nal_canal TEXT NOT NULL DEFAULT 'ambos',       -- 'whatsapp' | 'email' | 'ambos'
  nal_destinatario_nombre VARCHAR(200),
  nal_destinatario_telefono VARCHAR(20),
  nal_destinatario_correo VARCHAR(100),
  nal_placa VARCHAR(20),
  nal_mensaje TEXT,
  nal_programada_para TIMESTAMPTZ,   -- para el timer de 24h (NULL = inmediata)
  nal_enviada_en TIMESTAMPTZ,
  nal_error TEXT,                    -- mensaje de error si nal_estado = 'fallida'
  usr_creado_por BIGINT REFERENCES usuario(usr_id) ON DELETE SET NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT chk_nal_tipo CHECK (nal_tipo IN ('despachado_con_alerta', 'pendiente_expiracion')),
  CONSTRAINT chk_nal_estado CHECK (nal_estado IN ('pendiente', 'enviada', 'fallida')),
  CONSTRAINT chk_nal_canal CHECK (nal_canal IN ('whatsapp', 'email', 'ambos'))
);

CREATE INDEX IF NOT EXISTS idx_nal_tic_id ON notificacion_alerta(tic_id);
CREATE INDEX IF NOT EXISTS idx_nal_estado ON notificacion_alerta(nal_estado);
CREATE INDEX IF NOT EXISTS idx_nal_programada ON notificacion_alerta(nal_programada_para) WHERE nal_estado = 'pendiente';

CREATE TRIGGER trg_notificacion_alerta_updated_at
  BEFORE UPDATE ON notificacion_alerta
  FOR EACH ROW EXECUTE FUNCTION actualizar_updated_at();

-- Permisos RLS para notificacion_alerta
REVOKE ALL ON TABLE notificacion_alerta FROM anon;
GRANT SELECT, INSERT, UPDATE, DELETE ON TABLE notificacion_alerta TO authenticated;

CREATE POLICY admin_all ON notificacion_alerta FOR ALL TO authenticated
  USING (public.usuario_tiene_rol('administrador'))
  WITH CHECK (public.usuario_tiene_rol('administrador'));

CREATE POLICY gerencia_all ON notificacion_alerta FOR ALL TO authenticated
  USING (public.usuario_tiene_rol('gerencia'))
  WITH CHECK (public.usuario_tiene_rol('gerencia'));

CREATE POLICY despachador_select ON notificacion_alerta FOR SELECT TO authenticated
  USING (public.usuario_tiene_rol('despachador'));

CREATE POLICY despachador_insert ON notificacion_alerta FOR INSERT TO authenticated
  WITH CHECK (public.usuario_tiene_rol('despachador'));

