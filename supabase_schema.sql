CREATE OR REPLACE FUNCTION public.usuario_tiene_rol(p_rol TEXT)
RETURNS BOOLEAN LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public AS $$
  SELECT EXISTS (
    SELECT 1 FROM usuario u
    JOIN usuario_rol ur ON ur.usr_id = u.usr_id
    JOIN rol r ON r.rol_id = ur.rol_id
    WHERE u.auth_user_id = auth.uid() AND u.usr_estado = 'activo' AND r.rol_nombre = p_rol
  );
$$;

REVOKE ALL ON FUNCTION public.usuario_tiene_rol(TEXT) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.usuario_tiene_rol(TEXT) TO authenticated;

